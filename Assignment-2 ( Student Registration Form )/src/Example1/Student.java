package Example1;

public class Student 

{
         
	
	String Fname;
	String Lname;
	
	public Student(String fname, String lname) 
	
	{
		
		Fname = fname;
		Lname = lname;
	}

	public String getFname() 
	
	{
		return Fname;
	}

	public String getLname() 
	{
		return Lname;
	}
	
	
	
	
	
	
}
